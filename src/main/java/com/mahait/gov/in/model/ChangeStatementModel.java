package com.mahait.gov.in.model;

public class ChangeStatementModel {
	String empName;
	String componentName;
	String preBasic;
	String curBasic;
	String basicDiff;
	String color;
	String date;
	  
	
	
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getComponentName() {
		return componentName;
	}
	public void setComponentName(String componentName) {
		this.componentName = componentName;
	}
	public String getPreBasic() {
		return preBasic;
	}
	public void setPreBasic(String preBasic) {
		this.preBasic = preBasic;
	}
	public String getCurBasic() {
		return curBasic;
	}
	public void setCurBasic(String curBasic) {
		this.curBasic = curBasic;
	}
	public String getBasicDiff() {
		return basicDiff;
	}
	public void setBasicDiff(String basicDiff) {
		this.basicDiff = basicDiff;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	

}
